from .models import GenerateRequest, GeneratedFile


def clean(value: str) -> str:
    return textwrap.dedent(value).strip() + "\n"


def file(path: str, content: str) -> GeneratedFile:
    return GeneratedFile(path=path, content=clean(content))


def slug(request: GenerateRequest) -> str:
    return request.project_name.lower().replace("_", "-")


def python_requirements(request: GenerateRequest, framework: str) -> list[str]:
    requirements = [framework]
    if request.database == "postgresql":
        requirements.append("psycopg[binary]>=3.2,<4")
    elif request.database == "sqlite":
        requirements.append("sqlalchemy>=2.0,<3")
    if request.auth == "jwt":
        requirements.append("PyJWT>=2.10,<3")
    requirements.append("pytest>=8.4,<9")
    return requirements


def shared_files(request: GenerateRequest, framework_name: str) -> list[GeneratedFile]:
    features = "\n".join(f"- {feature}" for feature in request.features) or "- Health check"
    files = [
        file(
            ".env.example",
            f"""
            APP_NAME={request.project_name}
            APP_ENV=development
            DATABASE_URL=
            AUTH_SECRET=change-me
            """,
        ),
        file(
            ".gitignore",
            """
            .env
            .DS_Store
            node_modules/
            .next/
            dist/
            build/
            coverage/
            __pycache__/
            .pytest_cache/
            .venv/
            vendor/
            target/
            bin/
            obj/
            """,
        ),
        file(
            "README.md",
            f"""
            # {request.project_name}

            {request.description}

            Generated by DevForge as a {framework_name} starter.

            ## Selected architecture

            - Database: `{request.database}`
            - Authentication: `{request.auth}`
            - Tests: `{request.test_level}`
            - Package manager: `{request.package_manager}`

            ## Requested features

            {features}

            ## Start

            Copy `.env.example` to `.env`, install dependencies using the
            framework's package manager, and run the development command from
            the dependency manifest.

            ## Health check

            The starter exposes `/health` (or an equivalent health endpoint)
            and includes a smoke test.
            """,
        ),
    ]
    if request.docker:
        files.extend(docker_files(request))
    if request.ci:
        files.append(ci_file(request))
    return files


def docker_files(request: GenerateRequest) -> list[GeneratedFile]:
    commands = {
        "flask": ("python:3.12-slim", "pip install -r requirements.txt", "python -m app", 8000),
        "fastapi": (
            "python:3.12-slim",
            "pip install -r requirements.txt",
            "uvicorn app.main:app --host 0.0.0.0 --port 8000",
            8000,
        ),
        "django": (
            "python:3.12-slim",
            "pip install -r requirements.txt",
            "python manage.py runserver 0.0.0.0:8000",
            8000,
        ),
        "spring": ("maven:3.9-eclipse-temurin-21", "mvn package -DskipTests", "java -jar target/app.jar", 8080),
        "rails": ("ruby:3.3", "bundle install", "bundle exec rails server -b 0.0.0.0", 3000),
        "laravel": ("composer:2", "composer install --no-interaction", "php artisan serve --host=0.0.0.0", 8000),
        "aspnet": ("mcr.microsoft.com/dotnet/sdk:8.0", "dotnet restore", "dotnet run --urls http://0.0.0.0:8080", 8080),
    }
    if request.framework_id in commands:
        image, install, command, port = commands[request.framework_id]
        return [
            file(
                "Dockerfile",
                f"""
                FROM {image}
                WORKDIR /app
                COPY . .
                RUN {install}
                EXPOSE {port}
                CMD ["sh", "-c", "{command}"]
                """,
            )
        ]
    start = (
        "npm run start -- --host 0.0.0.0 --port 3000"
        if request.framework_id in {"react", "vue", "svelte"}
        else "npm run start -- --host 0.0.0.0"
        if request.framework_id == "angular"
        else "npm run start"
    )
    return [
        file(
            "Dockerfile",
            f"""
            FROM node:22-alpine
            WORKDIR /app
            COPY package*.json ./
            RUN npm install
            COPY . .
            RUN npm run build --if-present
            EXPOSE 3000
            CMD ["sh", "-c", "{start}"]
            """,
        )
    ]


def ci_file(request: GenerateRequest) -> GeneratedFile:
    if request.framework_id in {"flask", "fastapi", "django"}:
        steps = [
            "      - uses: actions/setup-python@v5",
            "        with:",
            '          python-version: "3.12"',
            "      - run: pip install -r requirements.txt",
            "      - run: pytest",
        ]
    elif request.framework_id == "spring":
        steps = [
            "      - uses: actions/setup-java@v4",
            "        with:",
            "          distribution: temurin",
            '          java-version: "21"',
            "      - run: mvn test",
        ]
    elif request.framework_id == "rails":
        steps = [
            "      - uses: ruby/setup-ruby@v1",
            "        with:",
            '          ruby-version: "3.3"',
            "          bundler-cache: true",
            "      - run: bundle exec rails test",
        ]
    elif request.framework_id == "laravel":
        steps = [
            "      - uses: shivammathur/setup-php@v2",
            "        with:",
            '          php-version: "8.3"',
            "      - run: composer install --no-interaction",
            "      - run: vendor/bin/phpunit",
        ]
    elif request.framework_id == "aspnet":
        steps = [
            "      - uses: actions/setup-dotnet@v4",
            "        with:",
            '          dotnet-version: "8.0.x"',
            "      - run: dotnet test tests/*.csproj",
        ]
    else:
        manager = request.package_manager
        prefix = f"{manager} " if manager != "npm" else "npm "
        steps = [
            "      - uses: actions/setup-node@v4",
            "        with:",
            '          node-version: "22"',
            f"      - run: {'corepack enable && ' if manager != 'npm' else ''}{prefix}install",
            f"      - run: {prefix}test -- --run",
            f"      - run: {prefix}run build --if-present",
        ]

    workflow = "\n".join(
        [
            "name: CI",
            "",
            "on:",
            "  push:",
            "  pull_request:",
            "",
            "jobs:",
            "  test:",
            "    runs-on: ubuntu-latest",
            "    steps:",
            "      - uses: actions/checkout@v4",
            *steps,
        ]
    )
    return file(".github/workflows/ci.yml", workflow)


def flask(request: GenerateRequest) -> list[GeneratedFile]:
    requirements = python_requirements(request, "Flask>=3.1,<4")
    return [
        file("requirements.txt", "\n".join(requirements)),
        file("pytest.ini", "[pytest]\npythonpath = .\ntestpaths = tests"),
        file(
            "app.py",
            f'''
            from flask import Flask, jsonify

            app = Flask(__name__)

            @app.get("/")
            def index():
                return jsonify(name="{request.project_name}", status="ready")

            @app.get("/health")
            def health():
                return jsonify(status="ok")

            if __name__ == "__main__":
                app.run(host="0.0.0.0", port=8000, debug=True)
            ''',
        ),
        file(
            "tests/test_app.py",
            """
            from app import app

            def test_health():
                response = app.test_client().get("/health")
                assert response.status_code == 200
                assert response.get_json() == {"status": "ok"}
            """,
        ),
        *shared_files(request, "Flask"),
    ]


