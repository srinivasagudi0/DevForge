def js_package(
    request: GenerateRequest,
    scripts: dict[str, str],
    dependencies: dict[str, str],
    dev_dependencies: dict[str, str],
    module: bool = True,
) -> GeneratedFile:
    package = {
        "name": slug(request),
        "version": "0.1.0",
        "private": True,
        **({"type": "module"} if module else {}),
        "scripts": scripts,
        "dependencies": dependencies,
        "devDependencies": dev_dependencies,
        "engines": {"node": ">=22"},
    }
    return file("package.json", json.dumps(package, indent=2))


def node_api(request: GenerateRequest, express: bool) -> list[GeneratedFile]:
    dependencies: dict[str, str] = {}
    entry = "src/server.js"
    if express:
        dependencies["express"] = "^5.1.0"
        source = f'''
        import express from "express";
        const app = express();
        app.use(express.json());
        app.get("/", (_req, res) => res.json({{ name: "{request.project_name}", status: "ready" }}));
        app.get("/health", (_req, res) => res.json({{ status: "ok" }}));
        if (process.env.NODE_ENV !== "test") app.listen(process.env.PORT || 3000);
        export default app;
        '''
        test = """
        import { describe, expect, it } from "vitest";
        import request from "supertest";
        import app from "../src/server.js";
        describe("health", () => it("responds", async () => {
          const response = await request(app).get("/health");
          expect(response.body).toEqual({ status: "ok" });
        }));
        """
        dev_dependencies = {"supertest": "^7.1.0", "vitest": "^3.2.0"}
    else:
        source = f'''
        import {{ createServer }} from "node:http";
        export const server = createServer((req, res) => {{
          res.setHeader("content-type", "application/json");
          if (req.url === "/health") return res.end(JSON.stringify({{ status: "ok" }}));
          res.end(JSON.stringify({{ name: "{request.project_name}", status: "ready" }}));
        }});
        if (process.env.NODE_ENV !== "test") server.listen(process.env.PORT || 3000);
        '''
        test = """
        import { afterAll, describe, expect, it } from "vitest";
        import { server } from "../src/server.js";
        afterAll(() => server.close());
        describe("server", () => it("is defined", () => expect(server).toBeDefined()));
        """
        dev_dependencies = {"vitest": "^3.2.0"}
    if request.auth == "jwt":
        dependencies["jsonwebtoken"] = "^9.0.2"
    if request.database == "postgresql":
        dependencies["pg"] = "^8.16.0"
    elif request.database == "mongodb":
        dependencies["mongodb"] = "^6.17.0"
    elif request.database == "sqlite":
        dependencies["better-sqlite3"] = "^12.0.0"
    return [
        js_package(
            request,
            {"dev": f"node --watch {entry}", "start": f"node {entry}", "test": "vitest run"},
            dependencies,
            dev_dependencies,
        ),
        file(entry, source),
        file("tests/health.test.js", test),
        *shared_files(request, "Express.js" if express else "Node.js"),
    ]


